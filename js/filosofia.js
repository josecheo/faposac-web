function ocultarMision(x) {
  x.style.display = "none";
const misionHover = document.getElementById('mision_container')
misionHover.style.display = 'flex'
}

function mostrarMision(x) {
  x.style.display = "flex";
  const misionHover = document.getElementById('mision_container')
misionHover.style.display = 'none'
}

